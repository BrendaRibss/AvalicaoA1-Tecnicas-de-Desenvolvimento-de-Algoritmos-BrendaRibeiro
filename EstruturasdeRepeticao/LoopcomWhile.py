# Exemplo usando While

# A variável de contagem é inicializada com o valor 1
num = 1

# Enquanto o número for menor ou igual a 100, o loop continua
while num <= 100:

    # Verifica se o número atual é par, é par caso o resto da divisão seja 0
    if num % 2 == 0:
  
        print(num)

    # Incrementa o valor da variável num em 1 para avançar a contagem
    num += 1
